    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel modeloTabla = (DefaultTableModel) jTable1.getModel();

        // Obtén el número de filas en la tabla
        int filas = modeloTabla.getRowCount();

        if (filas > 0) {
        try {
            conexionmysql con = new conexionmysql();
            Connection cn = con.conectar();

            for (int i = 0; i < filas; i++) {
                
                String codigo_suministro = (String) modeloTabla.getValueAt(i, 0);
                String codigo_proveedor = (String) modeloTabla.getValueAt(i, 1);
                String nombreproveedor = (String) modeloTabla.getValueAt(i, 2);
                String nombre_producto = (String) modeloTabla.getValueAt(i, 3);
               // double cantidad = Double.parseDouble(modeloTabla.getValueAt(i, 4).toString());
                try {  
                double cantidad = Double.parseDouble(modeloTabla.getValueAt(i, 2).toString());
               
                // Inserta los datos en la tabla de suministros
                String sql = "INSERT INTO Suministro (codigo_suministro, codigo_proveedor, nombreproveedor, nombre_producto, cantidad) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement preparedStatement = cn.prepareStatement(sql);
                preparedStatement.setString(1, codigo_suministro);
                preparedStatement.setString(2, codigo_proveedor);
                preparedStatement.setString(3, nombreproveedor);
                preparedStatement.setString(4, nombre_producto);
                preparedStatement.setDouble(5, cantidad);
                preparedStatement.executeUpdate();
                    
                } catch (NumberFormatException e) {
                    // Maneja el error cuando la cantidad no es un número
                    // Puedes mostrar un mensaje de error, omitir esta fila, etc.
                }
            }
            // Limpia el modelo de la tabla después de enviar los datos
            modeloTabla.setRowCount(0);
        } catch (SQLException e) {
            e.printStackTrace();
            // Maneja cualquier error de base de datos aquí
        }
    } else {
        // Muestra un mensaje de error si la tabla está vacía
        JOptionPane.showMessageDialog(this, "La tabla de suministros está vacía.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

