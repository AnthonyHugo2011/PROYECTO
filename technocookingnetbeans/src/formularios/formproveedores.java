    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String codigoSUMSeleccionado = (String) txtCodSum.getText();
        String codigoPROVSeleccionado = (String) jComboBoxCodigo.getSelectedItem();
        String nombrePROVSeleccionado = (String) jComboBoxNomProv.getSelectedItem();
        String nombrePRODSeleccionado = (String) jComboBoxProducto.getSelectedItem();
        String medida = (String) txtMedida.getText();
        double cantidad = Double.parseDouble(txtCantidad.getText());
        
        // Agrega una nueva fila a la tabla con los datos seleccionados.
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{codigoSUMSeleccionado, codigoPROVSeleccionado, nombrePROVSeleccionado, nombrePRODSeleccionado, medida, cantidad});

        // Limpia los campos después de agregar el pedido
        txtCodSum.setText("");
        jComboBoxCodigo.setSelectedIndex(0);
        jComboBoxNomProv.setSelectedIndex(0);
        jComboBoxProducto.setSelectedIndex(0);
        txtMedida.setText("");
        txtCantidad.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

