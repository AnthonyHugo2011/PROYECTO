    private void jComboBoxCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxCodigoActionPerformed
        try {
            conexionmysql con = new conexionmysql();
            Connection cn = con.conectar();
            String sql = "SELECT codigo_proveedor FROM Proveedor";
            PreparedStatement preparedStatement = cn.prepareStatement(sql);
            ResultSet resultSet = preparedStatement.executeQuery();

        while (resultSet.next()) {
            String codigo_proveedor = resultSet.getString("codigo_proveedor");
            jComboBoxCodigo.addItem(codigo_proveedor);
        }
            resultSet.close();
            preparedStatement.close();
            cn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jComboBoxCodigoActionPerformed

